/**
 * 
 * 1) var
 * 2) let
 * 3) const
 */

var name = "다시시작";
console.log(name);

var age = 32;
console.log(age);

let ive = '아이브';
console.log(ive);

/**
 * let과 var로 선언하면
 * 값 추후 변경 가능
 */

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);

// newJeans = '뉴진스 멤버 몰라'

/**
 * 선언과 할당
 * 
 * 1) 변수를 선언하는 것
 * 2) 할당
 */
var name; // 변수 선언
var name = 'ㅎㅇ'; // 할당
console.log(name);

